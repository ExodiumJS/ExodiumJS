class ConsoleCommandReader {
	constructor(server) {
		this.server = server;
		this.read();
	}

	read() {
		let CommandSender = new ConsoleCommandSender(this.server);
		let rl = readline.createInterface({
			input: process.stdin
		});

		rl.on("line", (input) => {
			this.server.getCommandMap().dispatch(CommandSender, input);
		});
	}
}

module.exports = ConsoleCommandReader;
